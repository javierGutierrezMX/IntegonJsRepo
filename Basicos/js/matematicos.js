function Suma(num1, num2) {
   var result = num1 + num2;
   return result;
}
function Resta(num1, num2) {
    var result =  num1 - num2;
    return result;
}
function Multi(num1, num2) {
    var result = num1 * num2;
    return result;
}
function Divi(num1, num2) {
    var result =  num2 != 0 ? num1 / num2 : "No se puede dividir entre 0";
    return result;
}